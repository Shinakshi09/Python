""" Removing the elements from the stack (using list) with pop method """



stack = []

stack.append(30)
stack.append(40)
stack.append(50)
stack.append(90)

print("Stacked list before removing the element", stack)

"""

output : [30, 40, 50, 90


"""

stack.pop()

"""
output :
90
"""

stack.pop()

"""
output : 50
"""

stack.pop()

"""
output : 40
"""


